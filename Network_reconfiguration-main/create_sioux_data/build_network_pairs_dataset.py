def print_dataset_stats(
    train_dataset: list,
    val_dataset: list,
    test_dataset: list,
) -> None:
    """
    Print summary statistics for all three dataset splits, for quick data quality check.

    Covers:
    - Sample counts and G'/G edge count distributions (to verify topology changes)
    - Normalized y (flows_new) value ranges (verifies scaler effect)
    - Mutation type distribution (checks 40/30/30 ratio)
    """
    from collections import Counter

    def _stats_one_split(dataset: list, name: str):
        e_old = [d.num_edges_old for d in dataset]
        e_new = [d.num_edges_new for d in dataset]
        y_all = torch.cat([d.y for d in dataset]).numpy()
        mutation_dist = Counter(d.mutation_type for d in dataset)

        print(f"\n  [{name}] {len(dataset)} samples")
        print(f"    G  edges: fixed {e_old[0]}")
        print(f"    G' edges: min={min(e_new)}, max={max(e_new)}, mean={np.mean(e_new):.1f}")
        print(f"    y (normalized flows_new): "
              f"min={y_all.min():.3f}, max={y_all.max():.3f}, "
              f"mean={y_all.mean():.3f}, std={y_all.std():.3f}")
        print(f"    Mutation type dist: {dict(mutation_dist)}")

    print(f"\n{'='*60}")
    print("Dataset Statistics Summary")
    print(f"{'='*60}")
    _stats_one_split(train_dataset, 'Train')
    _stats_one_split(val_dataset,   'Val')
    _stats_one_split(test_dataset,  'Test')


def validate_single_data_object(data: Data) -> None:
    """
    Perform assertion checks for field consistency and value validity on a single Data object.

    Used during debugging (run on the first sample of each split),
    Can be skipped in production to save time.

    Checks:
    - All required fields exist
    - Shapes match expectations
    - No NaN/Inf
    - edge_index node ids are in [0, 23]
    - non_centroid_mask content is correct
    """
    E_old = data.num_edges_old
    E_new = data.num_edges_new

    # Required field shape checks
    assert data.x.shape            == (NUM_NODES, 1),    f"x shape error: {data.x.shape}"
    assert data.edge_index_old.shape == (2, E_old),      f"edge_index_old shape error"
    assert data.edge_index_new.shape == (2, E_new),      f"edge_index_new shape error"
    assert data.edge_attr_old.shape  == (E_old, 3),      f"edge_attr_old shape error"
    assert data.flow_old.shape       == (E_old, 1),      f"flow_old shape error"
    assert data.edge_attr_new.shape  == (E_new, 3),      f"edge_attr_new shape error"
    assert data.y.shape              == (E_new, 1),      f"y shape error"
    assert data.non_centroid_mask.shape == (NUM_NODES,), f"non_centroid_mask shape error"
    assert data.net_demand.shape        == (NUM_NODES,), f"net_demand shape error: {data.net_demand.shape}"

    # Node id range check (must be in [0, NUM_NODES-1])
    for name, ei in [('edge_index_old', data.edge_index_old),
                     ('edge_index_new', data.edge_index_new)]:
        assert ei.min() >= 0,            f"{name} has negative node id"
        assert ei.max() < NUM_NODES,     f"{name} node id out of range (max={ei.max()})"

    # Value validity (NaN/Inf)
    for name, tensor in [('edge_attr_old', data.edge_attr_old),
                         ('flow_old',      data.flow_old),
                         ('edge_attr_new', data.edge_attr_new),
                         ('y',             data.y),
                         ('net_demand',    data.net_demand)]:
        assert not torch.isnan(tensor).any(), f"{name} contains NaN"
        assert not torch.isinf(tensor).any(), f"{name} contains Inf"

    # x must be all-ones (checks that no OD data is present)
    assert torch.all(data.x == 1.0), "x is not all-ones placeholder, possible OD data leak!"

    # non_centroid_mask: first 11 must be False (centroids), last 13 True (non-centroid)
    assert not data.non_centroid_mask[:11].any(), "First 11 nodes (centroids) should be False"
    assert data.non_centroid_mask[11:].all(),     "Last 13 nodes (non-centroid) should be True"

    # net_demand: non-centroid nodes should be ≈ 0 (within SUE solver numerical precision)
    # Since net_demand is now derived from flow divergence rather than the OD matrix,
    # non-centroid residuals may be non-zero due to finite SUE convergence tolerance.
    non_centroid_residual = data.net_demand[11:].abs().max().item()
    assert non_centroid_residual < 1.0, \
        f"net_demand[11:] (non-centroid) max residual {non_centroid_residual:.4f} exceeds 1.0 veh/hr"
    # net_demand dtype check
    assert data.net_demand.dtype == torch.float32, \
        f"net_demand dtype should be float32, got {data.net_demand.dtype}"


