def build_full_dataset(
    pairs: list,
    train_idx: np.ndarray,
    val_idx: np.ndarray,
    test_idx: np.ndarray,
    attr_scaler: StandardScaler,
    flow_scaler: StandardScaler,
) -> tuple:
    """
    Build a list of PyG Data objects for each of the three data splits (train/val/test).

    Both val and test splits must use the exact same scaler as train (i.e., trained on the training set),
    which is the standard ML best practice to avoid data leakage.

    Args:
        pairs:       All network pair data
        train_idx:   Training set indices
        val_idx:     Validation set indices
        test_idx:    Test set indices
        attr_scaler: Attribute scaler fit only on training set
        flow_scaler: Flow scaler fit only on training set

    Returns:
        train_dataset: list[Data]
        val_dataset:   list[Data]
        test_dataset:  list[Data]
    """
    def _build_split(idx_arr: np.ndarray, split_name: str) -> list:
        """Internal helper: build Data objects from index list."""
        dataset = []
        for idx in tqdm(idx_arr, desc=f"  Building {split_name} set"):
            data = build_single_data_object(pairs[idx], attr_scaler, flow_scaler)
            dataset.append(data)
        return dataset

    print(f"\n  Building training set ({len(train_idx)} samples)...")
    train_dataset = _build_split(train_idx, 'Train')

    print(f"\n  Building validation set ({len(val_idx)} samples)...")
    val_dataset = _build_split(val_idx, 'Val')

    print(f"\n  Building test set ({len(test_idx)} samples)...")
    test_dataset = _build_split(test_idx, 'Test')

    return train_dataset, val_dataset, test_dataset


def split_indices(
    num_samples: int,
    train_ratio: float = 0.6,
    val_ratio: float = 0.2,
    seed: int = 42,
) -> tuple:
    """
    Generate shuffled train/val/test split indices.

    First shuffle all indices (for randomness), then split by ratio (ensuring precise counts).
    test_ratio = 1 - train_ratio - val_ratio, not passed in to avoid floating point error.

    Args:
        num_samples: Total number of samples
        train_ratio: Training set ratio (default 0.6)
        val_ratio:   Validation set ratio (default 0.2)
        seed:        Random seed

    Returns:
        train_idx, val_idx, test_idx: np.ndarray of int
    """
    rng = np.random.default_rng(seed)
    all_idx = rng.permutation(num_samples)  # Shuffle

    n_train = int(num_samples * train_ratio)
    n_val   = int(num_samples * val_ratio)
    # test gets the residual, absorbs int() truncation rounding error
    train_idx = all_idx[:n_train]
    val_idx   = all_idx[n_train : n_train + n_val]
    test_idx  = all_idx[n_train + n_val:]

    return train_idx, val_idx, test_idx


