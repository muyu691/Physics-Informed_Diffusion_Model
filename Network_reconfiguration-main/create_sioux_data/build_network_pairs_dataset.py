def build_single_data_object(
    pair: dict,
    attr_scaler: StandardScaler,
    flow_scaler: StandardScaler,
) -> Data:
    """
    Convert a (G, G') data pair into a PyG Data object.

    Field definitions:
    ┌────────────────────┬──────────────┬──────────────────────────────────────────────────────────────┐
    │ Field              │ Shape        │ Description                                                  │
    ├────────────────────┼──────────────┼──────────────────────────────────────────────────────────────┤
    │ x                  │ [24, 1]      │ All-ones placeholder; OD data must NOT be here               │
    │ edge_index_old     │ [2, E_old]   │ Old graph connections, 0-indexed                             │
    │ edge_index_new     │ [2, E_new]   │ New graph connections, 0-indexed                             │
    │ edge_attr_old      │ [E_old, 3]   │ Normalized old physical attributes (cap, spd, len)           │
    │ flow_old           │ [E_old, 1]   │ Normalized historic flow, as demand proxy (model input)      │
    │ edge_attr_new      │ [E_new, 3]   │ Normalized new physical attributes (model input)             │
    │ y                  │ [E_new, 1]   │ Normalized new equilibrium flows, Ground Truth               │
    │ non_centroid_mask  │ [24]         │ bool, True for non-centroid nodes (12-24, 0-indexed 11-23)   │
    │ net_demand         │ [24]         │ float32, node net demand D_v in real flow space (veh/hr):    │
    │                    │              │   D_v = sum(inflow_old(v)) - sum(outflow_old(v))             │
    │                    │              │   Derived from flows_old divergence, NO OD matrix used       │
    │ num_nodes          │ int          │ = 24 (debug/assert)                                         │
    │ num_edges_old      │ int          │ = E_old                                                     │
    │ num_edges_new      │ int          │ = E_new                                                     │
    │ mutation_type      │ str          │ mutation type (debug/analysis)                              │
    └────────────────────┴──────────────┴──────────────────────────────────────────────────────────────┘

    Normalization steps:
    1. Old attributes [E_old, 3] → attr_scaler.transform → float32
    2. New attributes [E_new, 3] → attr_scaler.transform → float32
    3. flows_old [E_old, 1] → flow_scaler.transform → float32
    4. flows_new [E_new, 1] → flow_scaler.transform → float32

    Args:
        pair:         Single data pair dict output from solve_network_pairs.py
        attr_scaler:  Fitted StandardScaler for edge attributes
        flow_scaler:  Fitted StandardScaler for flows

    Returns:
        data: torch_geometric.data.Data object
    """
    # -- Extract edge attributes -------------------------------------------
    # extract_edge_attrs extracts in the fixed order of edge_list, aligns with flows_old/new index
    raw_attr_old = extract_edge_attrs(pair['G'],       pair['edge_list_old'])  # [E_old, 3]
    raw_attr_new = extract_edge_attrs(pair['G_prime'], pair['edge_list_new'])  # [E_new, 3]

    # -- Normalization -----------------------------------------------------
    # Note: Scaler is fit on training set, here we just transform (val/test also use the training scaler)

    # Normalize edge physical attributes
    norm_attr_old = attr_scaler.transform(raw_attr_old).astype(np.float32)  # [E_old, 3]
    norm_attr_new = attr_scaler.transform(raw_attr_new).astype(np.float32)  # [E_new, 3]

    # Normalize flows: reshape to [E, 1] before transform to keep 2D shape
    flows_old_2d = pair['flows_old'].reshape(-1, 1)                         # [E_old, 1]
    flows_new_2d = pair['flows_new'].reshape(-1, 1)                         # [E_new, 1]
    norm_flow_old = flow_scaler.transform(flows_old_2d).astype(np.float32)  # [E_old, 1]
    norm_flow_new = flow_scaler.transform(flows_new_2d).astype(np.float32)  # [E_new, 1]

    # -- edge_index (1-indexed → 0-indexed) --------------------------------
    ei_old = edge_list_to_index(pair['edge_list_old'])  # [2, E_old]
    ei_new = edge_list_to_index(pair['edge_list_new'])  # [2, E_new]

    # -- Node features: all-ones placeholder (24 × 1) ----------------------
    # Strictly follow .cursorrules Phase 2: the model will internally reinit with hidden_dim,
    # Here x is just a placeholder for PyG Data interface, never contains OD data.
    x = torch.ones((NUM_NODES, 1), dtype=torch.float)

    # -- Node mask: marks non-centroid nodes (for conservation loss) -------
    # Sioux Falls topology definitions:
    #   node 1-11 are centroids (OD sources/sinks, can generate/absorb flow)
    #   node 12-24 are intersections (must satisfy flow conservation)
    # After 0-indexed, nodes 12-24 correspond to indices 11-23, i.e. [11:]
    # This mask is retained for compatibility (e.g., analysis/logging), not used in loss anymore.
    non_centroid_mask = torch.zeros(NUM_NODES, dtype=torch.bool)
    non_centroid_mask[11:] = True

    # -- Net demand: D_v for each node (real flow space, unit: vehicles/hour) ----
    #
    # CRITICAL: NO OD MATRIX USED. We derive D_v purely from the old graph's
    # equilibrium flow field via spatial divergence (Kirchhoff's current law):
    #
    #   D_v = sum_{e ∈ In_old(v)} f_{e,old_real} - sum_{e ∈ Out_old(v)} f_{e,old_real}
    #
    # Physical interpretation:
    #   - At SUE equilibrium, flow conservation holds exactly for all nodes.
    #   - For non-centroid nodes (pure intersections): D_v ≈ 0 (within solver precision)
    #   - For centroid nodes (OD zones): D_v captures the net demand that was
    #     implicitly generated by the (unseen) OD matrix.
    #   This is mathematically equivalent to extracting D_v from the OD matrix,
    #   but requires NO access to the OD data whatsoever.
    #
    # flows_old is in REAL physical space (veh/hr), edge_list_old is 1-indexed.
    edge_arr_old = np.array(pair['edge_list_old'], dtype=np.int64)  # [E_old, 2]
    src_old = edge_arr_old[:, 0] - 1                                # [E_old], 0-indexed
    dst_old = edge_arr_old[:, 1] - 1                                # [E_old], 0-indexed
    flows_old_real = pair['flows_old'].flatten().astype(np.float64)           # [E_old], real veh/hr

    net_demand_np = np.zeros(NUM_NODES, dtype=np.float64)
    np.add.at(net_demand_np, dst_old, flows_old_real)               # inflow accumulation
    np.add.at(net_demand_np, src_old, -flows_old_real)              # outflow subtraction
    # D_v = sum(inflow) - sum(outflow) for each node

    net_demand = torch.from_numpy(net_demand_np.astype(np.float32)) # [24], float32

    # -- New-edge mask: identifies edges in G' that do NOT exist in G ------
    # Used for evaluation-phase analysis (Task 2: new vs old edge metrics).
    # new_edge_mask[i] = True means edge_list_new[i] is a newly added edge.
    old_edge_set = set(tuple(e) for e in pair['edge_list_old'])
    new_edge_mask = torch.tensor(
        [tuple(e) not in old_edge_set for e in pair['edge_list_new']],
        dtype=torch.bool,
    )  # [E_new]

    # -- Construct PyG Data object -----------------------------------------
    data = Data(
        # Node features
        x=x,                                                         # [24, 1]

        # Old graph structure and features
        edge_index_old=torch.from_numpy(ei_old).long(),              # [2, E_old]
        edge_attr_old=torch.from_numpy(norm_attr_old),               # [E_old, 3]
        flow_old=torch.from_numpy(norm_flow_old),                    # [E_old, 1]

        # New graph structure and features
        edge_index_new=torch.from_numpy(ei_new).long(),              # [2, E_new]
        edge_attr_new=torch.from_numpy(norm_attr_new),               # [E_new, 3]

        # Ground Truth
        y=torch.from_numpy(norm_flow_new),                           # [E_new, 1]

        # Physical constraint labels
        non_centroid_mask=non_centroid_mask,                         # [24], bool
        net_demand=net_demand,                                        # [24], float32, real-space D_v

        # Analysis mask
        new_edge_mask=new_edge_mask,                                 # [E_new], bool

        # Meta info (for debugging/statistics, not used in forward computation)
        num_nodes=NUM_NODES,
        num_edges_old=len(pair['edge_list_old']),
        num_edges_new=len(pair['edge_list_new']),
        mutation_type=pair['mutation_type'],
    )

    return data


