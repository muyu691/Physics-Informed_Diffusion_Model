"""
Phase 1 Final Step: Convert (G, G') network pairs into PyG Data objects and split the dataset

Input:
  network_pairs_dataset.pkl (generated by solve_network_pairs.py)
  Each pair contains: G, G_prime, flows_old, flows_new, edge_list_old, edge_list_new

Output:
  train_dataset.pt / val_dataset.pt / test_dataset.pt
  scalers/attr_scaler.pkl / scalers/flow_scaler.pkl

Fields of each PyG Data object (strictly following .cursorrules Phase 1 specification):
  x              : [24, 1]      All-ones placeholder node features (for model internal init)
  edge_index_old : [2, E_old]   Old network edge connections, 0-indexed
  edge_index_new : [2, E_new]   New network edge connections, 0-indexed
  edge_attr_old  : [E_old, 3]   Normalized physical edge attributes in the old network [capacity, speed, length]
  flow_old       : [E_old, 1]   Normalized historical flows in old network (proxy for demand, model input)
  edge_attr_new  : [E_new, 3]   Normalized physical edge attributes in the new network (model input)
  y              : [E_new, 1]   Normalized equilibrium flows in new network (Ground Truth)
  net_demand     : [24]         float32, node-level net demand D_v (vehicles/hour, real space)
                                  Derived from spatial divergence of flows_old on edge_list_old:
                                  D_v = sum(inflow_old(v)) - sum(outflow_old(v))
                                  NO OD MATRIX IS USED. The old equilibrium flows implicitly encode demand.
  num_nodes      : int          Number of nodes (for debugging)
  num_edges_old  : int          Number of edges in old network (for debugging)
  num_edges_new  : int          Number of edges in new network (for debugging)
  mutation_type  : str          Mutation type (for debugging)

Strict requirements:
  - ABSOLUTELY NO OD MATRIX: net_demand is computed from flows_old divergence, not od_matrix
  - No OD data present in x or any other field
  - Scaler is only fitted on the training set, val/test is always transformed using the training scaler (to prevent data leakage)
  - attr_scaler normalizes [capacity, speed, length] together (attributes of both old and new graphs share the same scaler)
  - flow_scaler normalizes [flows_old, flows_new] together (they represent the same physical quantity and distribution)
"""

import argparse
import os
import pickle
import sys
from datetime import datetime

import numpy as np
import torch
from sklearn.preprocessing import StandardScaler
from torch_geometric.data import Data
from tqdm import tqdm

NUM_NODES = 24  # Total number of nodes in Sioux Falls, fixed.


# ============================================================
# Utility Functions: Raw Feature Extraction
# ============================================================

def extract_edge_attrs(G, edge_list: list) -> np.ndarray:
    """
    Extract edge physical attributes from NetworkX DiGraph in the order of edge_list.

    The three extracted columns are: capacity, speed, length.
    Do not extract free_flow_time because it is derived from length/speed and is redundant.

    Args:
        G:         NetworkX DiGraph. Each edge contains 'capacity', 'speed', 'length' attributes
        edge_list: list[(u, v)]. The order of this list determines the output row order (aligns with flows_old/flows_new indices)

    Returns:
        attrs: np.ndarray [E, 3], column order is [capacity, speed, length]
    """
    caps   = np.array([G[u][v]['capacity'] for u, v in edge_list], dtype=np.float64)
    speeds = np.array([G[u][v]['speed']    for u, v in edge_list], dtype=np.float64)
    lens   = np.array([G[u][v]['length']   for u, v in edge_list], dtype=np.float64)
    # Stack columns in fixed order [capacity, speed, length]
    return np.column_stack([caps, speeds, lens])  # [E, 3]


def edge_list_to_index(edge_list: list) -> np.ndarray:
    """
    Convert edge_list to PyG format edge_index [2, E].

    Sioux Falls node ids are 1-24 (1-indexed), PyG requires 0-indexed, so subtract 1.

    Args:
        edge_list: list[(u, v)]. Node ids are 1-indexed

    Returns:
        edge_index: np.ndarray [2, E], dtype=int64, node ids are 0-indexed
    """
    if len(edge_list) == 0:
        return np.zeros((2, 0), dtype=np.int64)
    arr = np.array(edge_list, dtype=np.int64)  # [E, 2]
    arr -= 1                                    # 1-indexed → 0-indexed
    return arr.T                                # [2, E]


# ============================================================
# Scaler: Fitting and Saving
# ============================================================

def fit_scalers(pairs: list, train_idx: np.ndarray) -> tuple:
    """
    Fit scalers only on the training set to avoid validation/test information leakage.

    Fitting strategy:
    - attr_scaler (StandardScaler for [capacity, speed, length]):
        Stack all old and new edge attributes from training set samples vertically, then fit.
        Because attributes of old and new graphs represent the same physical quantities (capacity/speed/length),
        they assumed the same distribution, and using a shared scaler ensures consistent transformation.

    - flow_scaler (StandardScaler for flows):
        Combine flows_old and flows_new from all training samples and fit together.
        flows_old and flows_new are both equilibrium flows and share the same physics meaning/distribution.

    Args:
        pairs:     List of all data pairs (only using samples indexed by train_idx)
        train_idx: Array of training set sample indices

    Returns:
        attr_scaler: Fitted StandardScaler (for [capacity, speed, length])
        flow_scaler: Fitted StandardScaler (for flows, after reshape(-1, 1))
    """
    print("  Collecting training features for fitting StandardScaler...")

    all_attrs = []   # Store all training edge attributes [capacity, speed, length]
    all_flows = []   # Store all training edge flows

    for idx in train_idx:
        pair = pairs[idx]

        # --- Old network edge attributes ---
        old_attrs = extract_edge_attrs(pair['G'], pair['edge_list_old'])  # [E_old, 3]
        all_attrs.append(old_attrs)

        # --- New network edge attributes ---
        new_attrs = extract_edge_attrs(pair['G_prime'], pair['edge_list_new'])  # [E_new, 3]
        all_attrs.append(new_attrs)

        # --- Old network flows ---
        all_flows.append(pair['flows_old'].reshape(-1, 1))   # [E_old, 1]

        # --- New network flows (Ground Truth) ---
        all_flows.append(pair['flows_new'].reshape(-1, 1))   # [E_new, 1]

    # Vertically stack all training sample features
    all_attrs_np = np.vstack(all_attrs)  # [N_train_edges_total, 3]
    all_flows_np = np.vstack(all_flows)  # [N_train_flows_total, 1]

    print(f"  Training edge attribute matrix shape: {all_attrs_np.shape}")
    print(f"  Training flow matrix shape:          {all_flows_np.shape}")

    # Fit attr_scaler
    attr_scaler = StandardScaler()
    attr_scaler.fit(all_attrs_np)

    # Fit flow_scaler
    flow_scaler = StandardScaler()
    flow_scaler.fit(all_flows_np)

    print(f"  attr_scaler mean: {attr_scaler.mean_}")
    print(f"  attr_scaler std:  {attr_scaler.scale_}")
    print(f"  flow_scaler mean: {flow_scaler.mean_[0]:.2f}")
    print(f"  flow_scaler std:  {flow_scaler.scale_[0]:.2f}")

    return attr_scaler, flow_scaler


def save_scalers(attr_scaler: StandardScaler, flow_scaler: StandardScaler,
                 output_dir: str) -> None:
    """
    Save fitted scaler objects for future inverse_transform during model evaluation.

    Save paths:
      {output_dir}/scalers/attr_scaler.pkl  ← for edge_attr_old/edge_attr_new
      {output_dir}/scalers/flow_scaler.pkl  ← for flow_old/y (restore real flow values by inverse transform)

    Args:
        attr_scaler: Fitted attribute scaler
        flow_scaler: Fitted flow scaler
        output_dir:  Dataset root dir
    """
    scalers_dir = os.path.join(output_dir, 'scalers')
    os.makedirs(scalers_dir, exist_ok=True)

    with open(os.path.join(scalers_dir, 'attr_scaler.pkl'), 'wb') as f:
        pickle.dump(attr_scaler, f)
    with open(os.path.join(scalers_dir, 'flow_scaler.pkl'), 'wb') as f:
        pickle.dump(flow_scaler, f)

    print(f"  Scaler saved to: {scalers_dir}/")


